AddEventHandler('ud-apartments:enterApartmentserverside', function()
    TriggerServerEvent('ud-apartments:server:EnterApartment')
end)

RegisterNetEvent('ud-apartments:exitApartmentserverside')
AddEventHandler('ud-apartments:exitApartmentserverside', function()
